int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		n = s1[i] - s2[i];
		if (n)
			return (n);
		i++;
	}
	return (n);
}
