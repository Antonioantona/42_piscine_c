void	ft_rsort_int_tab(int *tab, int size)
{
	int	*x;
	int	i;

	i = 0;
	while (i < size)
	{
		x[i] = tab[size - i -1];
		i++;
	}
	i = 0;
	while (i < size)
	{
		tab[i] = x [i];
		i++;
	}
}
