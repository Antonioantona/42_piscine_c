char	*ft_strcapitalize(char *str)
{
	int	i;
	int	b;

	b = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		if ((str[i] >= 'a' && str[i] <= 'z') && b)
		{
			str[i] = str[i] - 32;
			b = 0;
		}
		else if (!(str[i] >= '0' && str[i] <= '9')
			&& !(str[i] >= 'a' && str[i] <= 'z'))
			b = 1;
		else if (str[i] >= '0' && str[i] <= '9')
			b = 0;
		i++;
	}
	return (str);
}
